package Ejercicio2;

public class Producto {
	
	private float precio;
	private String nombre;
	
	
	//Constructor
	public Producto(float precio,String nombre) {
		this.precio = precio;
		this.nombre = nombre;
	}


	public float getPrecio() {
		return this.precio;
	}


	public String getNombre() {
		return this.nombre;
	}
	
	
	
	

}
